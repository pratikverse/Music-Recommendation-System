def plot_heatmap(df_for_plot, features_for_plot):
    """
    Generates a heatmap visualizing the correlation between numeric features.
    Args:
        df_for_plot (DataFrame): The DataFrame containing the data.
        features_for_plot (list): List of numeric feature names.
    Returns:
        plotly.graph_objects.Figure: The heatmap figure.
    """
    corr = calculate_correlation(df_for_plot, features_for_plot)
    if corr.empty:
        st.warning("Cannot plot heatmap: No valid numeric features or empty correlation matrix.")
        return go.Figure()

    fig = go.Figure(data=go.Heatmap(
        z=corr.values,
        x=features_for_plot,
        y=features_for_plot,
        colorscale='RdBu',
        zmin=-1,
        zmax=1,
        text=np.round(corr.values, 2),
        texttemplate='%{text}',
        hoverongaps=False
    ))
    fig.update_layout(
        title='Feature Correlation Heatmap',
        width=700,
        height=700,
        xaxis_tickangle=-45
    )
    return fig

def plot_latent_3d(latent_features, df_for_plot):
    """
    Visualizes tracks in a 3D space using PCA, colored by genre.
    Args:
        latent_features (NumPy array): Latent features of the tracks.
        df_for_plot (DataFrame): DataFrame containing track information.
    Returns:
        plotly.graph_objects.Figure: The 3D scatter plot figure.
    """
    if latent_features.shape[0] == 0 or df_for_plot.empty:
        st.warning("Cannot plot 3D visualization: Latent features or DataFrame is empty.")
        return go.Figure()

    components = calculate_pca(latent_features)
    if components.shape[0] == 0: # Check if PCA returned an empty array
        return go.Figure() # Return empty figure if PCA failed or input was empty

    plot_df = pd.DataFrame(components, columns=['PC1', 'PC2', 'PC3'])
    
    # Ensure 'track_genre', 'track_name', 'artists' exist in df_for_plot
    # and align with the number of rows in components
    if 'track_genre' in df_for_plot.columns and len(df_for_plot) == len(components):
        plot_df['Genre'] = df_for_plot['track_genre']
    else:
        plot_df['Genre'] = 'Unknown' # Fallback
        st.warning("Track genre column not found or row count mismatch for 3D plot. Using 'Unknown' genre.")

    if 'track_name' in df_for_plot.columns and len(df_for_plot) == len(components):
        plot_df['Track'] = df_for_plot['track_name']
    else:
        plot_df['Track'] = 'Unknown Track'
    
    if 'artists' in df_for_plot.columns and len(df_for_plot) == len(components):
        plot_df['Artist'] = df_for_plot['artists']
    else:
        plot_df['Artist'] = 'Unknown Artist'

    fig = px.scatter_3d(plot_df, x='PC1', y='PC2', z='PC3',
                        color='Genre', hover_data=['Track', 'Artist'],
                        title="3D Interactive Visualization of Tracks by Genre",
                        labels={'PC1': 'First Principal Component',
                                'PC2': 'Second Principal Component',
                                'PC3': 'Third Principal Component'})
    fig.update_layout(
        scene=dict(
            xaxis_title='PC1',
            yaxis_title='PC2',
            zaxis_title='PC3'
        ),
        width=1200, # Adjusted width for better display
        height=800, # Adjusted height for better display
        showlegend=True,
        legend=dict(
            yanchor="top",
            y=0.99,
            xanchor="left",
            x=0.85
        )
    )
    return fig

def recommend_tracks(track_index, df, latent_features, knn, n_neighbors_knn=30, n_recommendations_display=9):
    """
    Recommends tracks similar to a given track.
    Args:
        track_index (int): Index of the track to find recommendations for.
        df (DataFrame): The original DataFrame (or processed one) with track details.
        latent_features (NumPy array): Latent features of all tracks.
        knn (NearestNeighbors): Trained KNN model.
        n_neighbors_knn (int): Number of neighbors to query from KNN.
        n_recommendations_display (int): Number of unique recommendations to display.
    Returns:
        DataFrame: DataFrame containing recommended tracks.
    """
    if not (0 <= track_index < len(df)):
        st.error(f"Selected track index ({track_index}) is out of bounds. Please select a valid track.")
        return pd.DataFrame()

    if latent_features.shape[0] == 0 or knn is None:
        st.warning("Latent features or KNN model not loaded. Cannot provide recommendations.")
        return pd.DataFrame()

    track_vector = latent_features[track_index].reshape(1, -1)
    distances, indices = knn.kneighbors(track_vector, n_neighbors=n_neighbors_knn)
    
    # Flatten indices and remove the first one (which is the track itself)
    similar_indices = indices.flatten()[1:]
    
    # Ensure indices are within bounds of df
    valid_similar_indices = [idx for idx in similar_indices if idx < len(df)]

    similar_tracks = df.iloc[valid_similar_indices]
    
    # Drop duplicates by track name and artist, then take the top N
    unique_tracks = similar_tracks.drop_duplicates(subset=['track_name', 'artists']).head(n_recommendations_display)
    
    return unique_tracks[['track_name', 'artists', 'track_genre', 'track_id']] # Include track_id for embedding

