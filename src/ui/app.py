@st.cache_data
def calculate_pca(latent_features, n_components=3):
    """Performs PCA on latent features for visualization."""
    if latent_features.shape[0] == 0:
        st.warning("Cannot perform PCA: Latent features array is empty.")
        return np.array([])
    pca = PCA(n_components=n_components)
    components = pca.fit_transform(latent_features)
    return components

@st.cache_data
def calculate_correlation(df, numeric_features):
    """Calculates the correlation matrix for numeric features."""
    # Ensure all numeric features exist in the DataFrame before calculating correlation
    existing_numeric_features = [f for f in numeric_features if f in df.columns]
    if not existing_numeric_features:
        st.warning("No numeric features found for correlation calculation.")
        return pd.DataFrame() # Return empty DataFrame if no features exist
    return df[existing_numeric_features].corr()


